#include "libft.h"

char	*ft_strncat(char *restrict s1, const char *restrict s2, size_t n)
{
	size_t	i;
	size_t	k;

	i = 0;
	k = 0;
	while (*(s1 + k))
		k++;
	while (i < n && *(s2 + i))
	{
		*(s1 + k) = *(s2 + i);
		k++;
		i++;
	}
	*(s1 + k) = '\0';
	return (s1);
}
